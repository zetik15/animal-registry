#!/bin/bash

# 1. Создание файлов с домашними и вьючными животными
echo "Создаем файл с домашними животными"
cat > Домашние_животные << EOF
Собаки
Кошки
Хомяки
EOF

echo "Создаем файл с вьючными животными"
cat > Вьючные_животные << EOF
Лошади
Верблюды
Ослы
EOF

# Просмотр содержимого файлов
echo "Содержимое файла 'Домашние_животные':"
cat Домашние_животные

echo "Содержимое файла 'Вьючные_животные':"
cat Вьючные_животные

# Объединение файлов
echo "Объединяем файлы"
cat Домашние_животные Вьючные_животные > Животные

echo "Содержимое объединенного файла 'Животные':"
cat Животные

# Переименование файла
echo "Переименовываем файл 'Животные' в 'Друзья_человека'"
mv Животные Друзья_человека

echo "Содержимое файла 'Друзья_человека':"
cat Друзья_человека

# 2. Создание директории и перемещение файла
echo "Создаем директорию 'Питомник'"
mkdir -p Питомник

echo "Перемещаем файл 'Друзья_человека' в директорию 'Питомник'"
mv Друзья_человека Питомник/

echo "Проверяем, что файл перемещен:"
ls -la Питомник/

# 3. Подключение репозитория MySQL и установка пакета
echo "Добавляем репозиторий MySQL"
sudo apt-get update
sudo apt-get install -y wget
wget https://dev.mysql.com/get/mysql-apt-config_0.8.24-1_all.deb
sudo dpkg -i mysql-apt-config_0.8.24-1_all.deb
sudo apt-get update

echo "Устанавливаем пакет mysql-server из репозитория MySQL"
sudo apt-get install -y mysql-server

# 4. Установка и удаление deb-пакета с помощью dpkg
echo "Скачиваем пакет для установки (в качестве примера используем htop)"
wget http://archive.ubuntu.com/ubuntu/pool/universe/h/htop/htop_3.0.5-7_amd64.deb

echo "Устанавливаем пакет с помощью dpkg"
sudo dpkg -i htop_3.0.5-7_amd64.deb

echo "Проверяем, что пакет установлен"
dpkg -l | grep htop

echo "Удаляем пакет с помощью dpkg"
sudo dpkg -r htop

echo "Проверяем, что пакет удален"
dpkg -l | grep htop
